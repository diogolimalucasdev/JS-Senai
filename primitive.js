// tipos primitivos


// string 
let saudacao = 'hello, world'
console.log(saudacao)
console.log(typeof saudacao, 'string, saudacao')

//number
let age = 27
console.log(age)
console.log(typeof age, 'number, age ')

let price = 19.99
console.log(price)
console.log(typeof price, 'number, price')

//boolean
let ligado = true
console.log(ligado)
console.log(typeof ligado, 'boolean true')

let desligado = false
console.log(desligado)
console.log(typeof desligado, 'boolean falso')

//null
let resultado = null
console.log(resultado)
console.log(typeof resultado, 'null')


//undefined
let status
console.log(status)
console.log(typeof status, 'undefined, status')
